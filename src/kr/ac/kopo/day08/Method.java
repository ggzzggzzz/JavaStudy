package kr.ac.kopo.day08;

public class Method {

	void call() {
		System.out.println("call() 메소드 호출");
	}
	
	
	
	void call(int i) {
		System.out.println("call2(" + i +") 메소드 호출" );
	}
	void call(double i) {
		System.out.println("call2(" + i +") 메소드 호출" );
	}
	void call(String i) {
		System.out.println("call2(" + i +") 메소드 호출" );
	}
	
}
